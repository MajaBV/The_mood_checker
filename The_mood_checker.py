mood = input("What is your mood at the moment? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't be so, you have many more opportunities.")
elif mood == "excited":
    print("Where are you going again?")
elif mood == "relaxed":
    print("Sounds great.")
else:
    print("I don't recognize this mood.")